package com.starczi;

public class Main {

    public static void main(String[] args) {
        int myI = 5;
        float myF =5f;
        double myD = 5d;
        System.out.println("My int = "+myI);
        System.out.println("My float = "+myF);
        System.out.println("My double = "+myD);

        double myK = 150d;
        double myP = myK*0.45359237d;
        System.out.println("My kilos = "+myK);
        System.out.println("My pounds = "+myP);
    }
}
